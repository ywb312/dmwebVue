<template>
    <div class="plan page">
        <!-- 标题  -->
        <mt-header :title="pageData.text" fixed>
            <router-link to slot="left">
                <mt-button icon="back" @click="$router.back(-1)"></mt-button>
            </router-link>
        </mt-header>
        <!-- 主体 -->
        <div class="wrap">
            <component ref="child" :is="pageData.components" :pageData="pageData"></component>
        </div>
    </div>
</template>
<script>
import myPlan from "@/components/plan/myPlan.vue";
import searchPlan from "@/components/plan/searchPlan.vue";
import special from "@/components/plan/special.vue";
export default {
    name: "plan",
    data() {
        return {
            // 页面配置
            pageData: "",
            page: [
                {
                    text: "检查计划",
                    id: "myPlan",
                    components: "myPlan"
                },
                {
                    text: "计划查询",
                    id: "scdetail",
                    components: "searchPlan"
                },
                {
                    text: "专项检查计划",
                    id: "special",
                    components: "special"
                }
            ]
        };
    },
    created() {
        this.getPageData();
    },
    methods: {
        getPageData() {
            let id = this.$route.query.id;
            this.page.forEach(item => {
                if (item.id == id) {
                    this.pageData = item;
                }
            });
        }
    },
    components: {
        myPlan,
        searchPlan,
        special
    }
};
</script>
